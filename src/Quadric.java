public class Quadric {
    private double a, b, c, d, e, f, g, h, i, j;

    public Quadric(double a, double b, double c, double d, double e, double f, double g, double h, double i, double j) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
        this.e = e;
        this.f = f;
        this.g = g;
        this.h = h;
        this.i = i;
        this.j = j;
    }
}